#crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada

numero = int(input('digite um númeo inteiro: '))

print('o dobro do seu número é {}' .format(numero * 2 / 10))
print('o triplo do seu número é {}' .format(numero * 3))
print('a raiz quadrada do seu número é {:.3f}' .format(numero ** (1/2)))